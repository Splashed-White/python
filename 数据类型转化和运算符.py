# ===========================数据类型转换==============================
num = input('请输入一个数字:')
print(f'用户输入的数字为:{num}')
print(type(num)) # <class 'st1r'>
print(type(int(num))) # <class 'int'>

x = 1
print(type(x)) # <class 'int'>
print(type(float(x))) # <class 'float'>

